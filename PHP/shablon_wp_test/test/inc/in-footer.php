function get_test_copyright( ) {

	// data+company
	echo '<p>© 2012–' . current_time( 'Y' ) . ' ЗАО «Штучки»</p>'; 
	
	// email admin
	if ( get_bloginfo( 'admin_email' ) ) {
		echo '<p>' . get_bloginfo( 'admin_email' ) . '</p>';
	}

};

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//	Разраб инфо, return: <img>logo, <div><p><a>link<p>title	//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

function get_test_proginfo( ) {

	$about = get_page_by_path( 'about' );
	
	// logo
	echo '<img src="/img/small_logo.png" >';
	
	// Ссылка
	echo build_test_link( [	"before"		=>	'<div><p>',
						"href"		=>	get_permalink( $about ),
						"classID"		=>	null,
						"title"		=>	"О нас",
						"body"		=>	"Разработка сайта",
						"icon_name"	=>	null,
						"icon_classID"	=>	null,
						"after"		=>	'</p><p>"Штучки плюс"</p></div>',
					] );
};

